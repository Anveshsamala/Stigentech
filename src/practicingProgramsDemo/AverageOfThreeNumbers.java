package practicingProgramsDemo;

public class AverageOfThreeNumbers {

	public static void main(String[] args) {
		double x=20;
		double y=55;
		double z=85;
		double sum=x+y+z;
		//Average of three numbers
		double average=sum/3;
		System.out.println("average of three numbers= "+average);

	}

}
